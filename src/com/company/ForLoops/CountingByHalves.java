package com.company.ForLoops;

public class CountingByHalves {
    public static void main(String[] args) {

        System.out.println("x");
        System.out.println("------");



        for (float x = -10.0f; x <= 10.0f; x += 0.5f)
        {
            System.out.println(x);
        }

    }
}